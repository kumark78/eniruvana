    function checkEmail(email) {
        var re = /^(([^<>()[\]\\.,;:\s@\"]+(\.[^<>()[\]\\.,;:\s@\"]+)*)|(\".+\"))@(([^<>()[\]\\.,;:\s@\"]+\.)+[^<>()[\]\\.,;:\s@\"]{2,})$/i;
        return re.test(email);
    }

    function RefreshImage(valImageId)
    {
        var objImage = document.images[valImageId];
        if (objImage == undefined) 
        {
            return;
        }
        var now = new Date();
        objImage.src = objImage.src.split('?')[0] + '?x=' + now.toUTCString();
    }

    function checkform() 
    {
        // Clear previous error messages
        document.getElementById('errmsg').innerHTML = '';
        
        // Get form elements
        var name = document.en.nam.value.trim();
        var company = document.en.cname.value.trim();
        var address = document.en.address.value.trim();
        var phone = document.en.phone.value.trim();
        var email = document.en.emailid.value.trim();
        var requirement = document.en.requir.value.trim();
        var captcha = document.en.captchacode.value.trim();
        
        // Validate Name
        if (name === "") {
            alert("Please enter your Name");
            document.en.nam.focus();
            return false;
        }
        
        // Validate Company Name
        if (company === "") {
            alert("Please enter your Company Name");
            document.en.cname.focus();
            return false;
        }
        
        // Validate Address
        if (address === "") {
            alert("Please enter your Address");
            document.en.address.focus();
            return false;
        }
        
        // Validate Phone
        if (phone === "") {
            alert("Please enter your Contact Number");
            document.en.phone.focus();
            return false;
        } else if (isNaN(phone)) {
            alert("Please enter a valid Contact Number");
            document.en.phone.focus();
            return false;
        } else if ((phone.length < 10) || (phone.length > 12)) {
            alert("Please enter your Contact Number which should not be lesser than 10 digits and greater than 12 digits");
            document.en.phone.focus();
            return false;
        }
        
        // Validate Email
        if (email === "") {
            alert("Please enter your Email");
            document.en.emailid.focus();
            return false;
        } else if (!checkEmail(email)) {
            alert("Please enter a valid Email ID");
            document.en.emailid.focus();
            return false;
        }
        
        // Validate Requirement
        if (requirement === "") {
            alert("Please enter your requirement");
            document.en.requir.focus();
            return false;
        }
        
        // Validate CAPTCHA
        if (captcha === "") {
            alert("Please enter the CAPTCHA code.");
            document.en.captchacode.focus();
            return false;
        }

        // jQuery AJAX captcha verification
        $.get('captchaverify.php', { captchacode: captcha }, function(resp) {
            var isValid = (String(resp).toLowerCase().indexOf('true') !== -1);
            if (!isValid) {
                RefreshImage('imgCaptcha');
                document.getElementById('errmsg').innerHTML = 'You entered the wrong code. Please try again.';
                document.en.captchacode.value = '';
                document.en.captchacode.focus();
                return;
            } else {
                document.getElementById('errmsg').innerHTML = '✓ CAPTCHA verified successfully!';
                document.en.method = 'post';
                document.en.action = 'contact-ussave.php';
                document.en.submit();
            }
        }).fail(function() {
            document.getElementById('errmsg').innerHTML = 'Error verifying CAPTCHA. Please try again.';
        });
    }
